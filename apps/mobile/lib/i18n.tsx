type TranslationKey = keyof (typeof translations)['en'];

type I18nContextValue = {
  language: AppLanguage;
  setLanguage: (language: AppLanguage) => void;
  t: (key: TranslationKey, variables?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

function detectInitialLanguage(): AppLanguage {
  try {
    const locale = Intl.DateTimeFormat().resolvedOptions().locale.toLowerCase();
    if (locale.startsWith('ru')) {
      return 'ru';
    }
  } catch {
    return 'en';
  }

  return 'en';
}

function translate(language: AppLanguage, key: TranslationKey, variables?: Record<string, string | number>) {
  const template = translations[language][key] ?? translations.en[key];

  if (!variables) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_, variableName: string) => {
    return String(variables[variableName] ?? `{${variableName}}`);
  });
}

export function I18nProvider({ children }: PropsWithChildren) {
  const [language, setLanguage] = useState<AppLanguage>(detectInitialLanguage);

  const value = useMemo<I18nContextValue>(() => {
    return {
      language,
      setLanguage,
      t: (key, variables) => translate(language, key, variables),
    };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error('useI18n must be used inside I18nProvider.');
  }

  return context;
}

export function getLanguageLabel(language: AppLanguage) {
  return languageOptions.find((item) => item.value === language)?.label ?? 'English';
}

export function getDateLocale(language: AppLanguage) {
  return language === 'ru' ? 'ru-RU' : 'en-US';
}
